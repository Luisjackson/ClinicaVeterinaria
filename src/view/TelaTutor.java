    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
 
        String nomeSelecionado = (String) jComboBox1.getSelectedItem();
        Tutor tutorSelecionado = null;

        for (Tutor t : Sistema.listaTutores) {
            if (t.getNome().equals(nomeSelecionado)) {
                tutorSelecionado = t;
                break;
            }
        }

        if (tutorSelecionado != null) {
            TelaConsultarTutor painel = new TelaConsultarTutor(tutorSelecionado);
            JFrame frame = new JFrame("Informações do Tutor");
            frame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            frame.getContentPane().add(painel);
            frame.pack();
            frame.setLocationRelativeTo(null);
            frame.setVisible(true);
        } else {
             javax.swing.JOptionPane.showMessageDialog(this, "Tutor não encontrado.");
        }
    
    
   
    }//GEN-LAST:event_jButton3ActionPerformed

